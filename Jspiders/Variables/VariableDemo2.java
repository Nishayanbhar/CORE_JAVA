/*
	Declare 3 var and perform following operations:
	1. calculate Sum;
	2. calculate avg;
	3. print sum & avg on console.
*/
class VariableDemo2
{
	public static void main(String args[])
	{
		double no1=85.5,no2=70.3,no3=67.4; //one step initialization;
		double sum,avg;
		
		sum=no1+no2+no3;
		avg=sum/3;
		
		System.out.println("Sum:"+sum);
		System.out.println("Avg:"+avg);
		
		
		
		
		
	}
}