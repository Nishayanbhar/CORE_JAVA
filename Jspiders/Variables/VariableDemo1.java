/* Print student details
	1.Var declaration
	2.Var initialization
	3.Print
*/
class VariableDemo1
{
	public static void main(String args[])
	{
	//declaration
		int stud_id;
		String stud_name;
		double stud_per;
		char stud_grade;
		
	//initialization
		stud_id=1;
		stud_name="Nisha";
		stud_per=80;
		stud_grade='A';
		
	//Print 
		System.out.println("Student ID:"+stud_id);
		System.out.println("Student name:"+stud_name);
		System.out.println("Student Percentage:"+stud_per);
		System.out.println("Student Grade:"+stud_grade);
			
	}
}